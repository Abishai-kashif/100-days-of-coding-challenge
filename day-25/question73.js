// Assigning and Updating Variables :       - Question 73
// created a function
function updateVal() {
    // inside func declared a variable using let and assigned some initial value
    var value = "I am initial value";
    console.log("Initial Value : ".concat(value)); // logs the initial val
    // updating the func value within the same func
    value = "I am updated value";
    console.log("Updated Value : ".concat(value)); // logs the updated val
}
updateVal();
// this function tells how initialize and update variables value
