// Converting to Upper & Lower Case:      - Question 83

// this func takes String & convert it to Upper case & Lower case
function convertToUpperAndLower(str: string) {
	// converting to Upper case & printing it
	console.log(`In Upper case : ${str.toUpperCase()}`);

	// converting to Lower case & printing it
	console.log(`In Lower case : ${str.toLowerCase()}`);
}

// calling the function
convertToUpperAndLower("Abishai");
