// function Parameters and Return Values :      - Question 76

// created a function that takes two numbers as argument
function sumTwoNums(num1: number, num2: number): number {
	// in func adding params together & returning the result;
	return num1 + num2;
}

// calling function with two nums as argument, this func returns the result
// and we are simply logs the returned value.

console.log(`The Result: ${sumTwoNums(2, 2)}`); // output: 4
